def sumar(op1, op2):
	print(f"El resultado de la suma es: {op1+op2}")

def restar(op1, op2):
	print(f"El resultado de la resta es: {op1-op2}")

def multiplicar(op1, op2):
	print(f"El resultado de la multiplicación es: {op1*op2}")

def dividir(dividendo, divisor):
	print(f"El resultado de la división es: {dividendo/divisor}")

def potencia(base, exponente):
	print(f"El resultado de la potencia es: {base ** exponente}")

def redondear(numero):
	print(f"El resultado de la multiplicación es: {round(numero)}")